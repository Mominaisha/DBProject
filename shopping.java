private double selectedProductPrice = 0;

private void initTableSelectionListener() {
    jTable1.getSelectionModel().addListSelectionListener(event -> {
        if (!event.getValueIsAdjusting()) {
            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow != -1) {
                // Get the price of the selected product
                selectedProductPrice = (double) jTable1.getValueAt(selectedRow, 2); // Assuming price is in column 2
                calculateTotal(); // Update total immediately
            }
        }
    });
}
private void calculateTotal() {
    try {
        int quantity = Integer.parseInt(jTextField3.getText()); // Quantity entered by user
        double total = quantity * selectedProductPrice; // Calculate total
        jTextField1.setText(String.format("%.2f", total)); // Display total in the total field
    } catch (NumberFormatException e) {
        jTextField1.setText("0.00"); // Reset total if input is invalid
    }
}




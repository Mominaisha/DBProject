    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);  // Clear existing data in the table

    String url = "jdbc:mysql://localhost:3306/onlineShopping?useSSL=false";
    String mysqluser = "root";
    String mysqlpwd = "hi12fz34a5.#";
    String query = "SELECT p_id, name, price, Category FROM product";

    try (Connection con = DriverManager.getConnection(url, mysqluser, mysqlpwd);
         Statement stmnt = con.createStatement();
         ResultSet rs = stmnt.executeQuery(query)) {
        while (rs.next()) {
            int pid = rs.getInt("p_id");
            String pname = rs.getString("name");
            double price = rs.getDouble("price");
            String category = rs.getString("Category");

            // Add the retrieved product to the table
            model.addRow(new Object[]{pid, pname, price, category});
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error in viewing data: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

